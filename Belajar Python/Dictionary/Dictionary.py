# Belajar Dictionary
# List adalah contoh dari arry , mengakses data menggunakan index

# Dictionary (dict) -> associative array
# identifier  -> key
# Menggunakan 'key':'Value'

list_data = ["oyen","kyomeng","asep"]

data_dict = {
    'key':'value',
    'ye':'oyen',
    'ab':'siabu',
    'om':'komeng',
    'nmbr':1000,
    'list':list_data
}

print(data_dict['ye'])
print(data_dict['nmbr'])
print(data_dict['list'])
